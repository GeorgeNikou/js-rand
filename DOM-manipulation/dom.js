// How to target elements on an html page

// targets an ID
var tag = document.getElementById("highlight");
// targets a class
var tags = document.getElementsByClassName("bolded");
// targets a selector
var tagName = document.getElementsByTagName("li");
// targets first of anything
var tagq = document.querySelector("#highlight");
// targets all of anything
